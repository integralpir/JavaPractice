class differentMtds {
    public static void main(String args[]){
        int x = 2;
        int y = 3;
        System.out.println(sum(x, y));
        System.out.println(mlt(x, y));
    }

    public static int sum(int a, int b){
        return a + b;
    }

    public static int mlt(int a, int b){
        return a * b;
    }
}